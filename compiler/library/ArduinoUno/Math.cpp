#include "Math.h"

int Math::min(int lval, int rval){
    return std::min(lval, rval);
}

float Math::min(float lval, float rval){
    return std::min(lval, rval);
}

int Math::max(int lval, int rval){
    return std::max(lval, rval);
}

float Math::max(float lval, float rval){
    return std::max(lval, rval);
}

float Math::sin(float val){
    return std::sin(val);
}

float Math::sin(int val){
    return std::sin(val);
}

float Math::cos(float val){
    return std::cos(val);
}

float Math::cos(int val){
    return std::cos(val);
}

